dia = int(input("Digite um número de 1 a 7: "))

if dia == 1:
    print("Hoje é Domingo.")
elif dia == 2:
    print("Hoje é Segunda.")
elif dia == 3:
    print("Hoje é Terça.")
elif dia == 4:
    print("Hoje é Quarta.")
elif dia == 5:
    print("Hoje é Quinta.")
elif dia == 6:
    print("Hoje é Sexta.")
elif dia == 7:
    print("Hoje é Sábado.")
else:
    print("Digite um número Válido!")